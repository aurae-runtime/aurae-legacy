package api

import (
	"github.com/kris-nova/aurae/api/object"
	"github.com/kris-nova/aurae/pkg/common"
)

type Application struct {
	object.Object

	Image string
}

func NewApplication(raw, node, domain string) *Application {
	fqn := common.ParseRawName(raw, node, domain)
	return &Application{
		Object: object.Object{
			Access: object.Access{},
			Meta: object.Meta{
				Name: fqn,
			},
			Metrics: object.Metrics{},
		},
		Image: fqn,
	}
}
