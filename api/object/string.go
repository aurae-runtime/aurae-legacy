package object

import (
	"encoding/json"
	"fmt"
)

func PrettyPrint(x any) string {
	data, err := json.MarshalIndent(x, "", "  ")
	if err != nil {
		return fmt.Sprintf("Unable to Marshal: %v.\nRaw Dump: \n\n%+v\n\n", err, x)
	}
	return string(data)
}
