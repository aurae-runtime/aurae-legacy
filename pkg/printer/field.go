package printer

type Field struct {
	Header string
	i      int
	values map[int]any
	width  int
}

func (t *Table) NewField(header string) *Field {
	return &Field{
		Header: header,
		i:      0,
		values: make(map[int]any),
		width:  18, // TODO Calculate by dividing terminal size
	}
}

func (f *Field) AddValue(v any) {
	f.values[f.i] = v
	f.i++
}
