package name

// Name is a simplified naming mechanism for Aurae nodes.
//
// Name consists of coupling both servicenames and hostnames
// into the same meta resource.
//
// These names can be used for identifying instances in the
// various mesh networks.
type Name struct {
	Servicename
	Hostname
	raw string
}

func New(any string) Name {
	host := NewHostname(any)
	svc := NewServicename(any)
	return Name{
		raw:         any,
		Hostname:    host,
		Servicename: svc,
	}
}

func (n Name) String() string {
	return n.Service()
}
