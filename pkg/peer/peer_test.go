func TestPeerToPeerConnectSingle(t *testing.T) {
	a := NewPeerServicename("a", nil)
	b := NewPeerServicename("b", nil)

	var err error
	_, err = a.Establish()
	if err != nil {
		t.Errorf("unable to establish a: %v", err)
	}
	_, err = b.Establish()
	if err != nil {
		t.Errorf("unable to establish b: %v", err)
	}

}
