package printers

import (
	"encoding/json"
	"fmt"
)

func SimpleStdout(x any) {
	data, err := json.MarshalIndent(x, "", "  ")
	if err != nil {
		fmt.Printf("Unable to Marshal: %v.\nRaw Dump: \n\n%+v\n\n", err, x)
		return
	}
	fmt.Printf("%s\n", string(data))
}
